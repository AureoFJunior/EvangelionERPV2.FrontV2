const mergedDictionaries: Record<AppLanguage, TranslationDict> = {
  en: { ...dictionaries.en, ...dictionaryExtensions.en },
  pt: { ...dictionaries.pt, ...dictionaryExtensions.pt },
  es: { ...dictionaries.es, ...dictionaryExtensions.es },
  ja: { ...dictionaries.ja, ...dictionaryExtensions.ja },
};

const I18nContext = createContext<I18nContextValue | undefined>(undefined);

const interpolate = (input: string, vars?: Record<string, string | number>) => {
  if (!vars) {
    return input;
  }
  return Object.entries(vars).reduce(
    (acc, [key, value]) => acc.replace(new RegExp(`\\{${key}\\}`, 'g'), String(value)),
    input,
  );
};

export function I18nProvider({ children }: { children: ReactNode }) {
  const { user } = useAuth();
  const language = normalizeLanguageCode(user?.language) ?? 'en';

  const value = useMemo<I18nContextValue>(
    () => ({
      language,
      t: (key, vars) => interpolate(mergedDictionaries[language][key] ?? key, vars),
    }),
    [language],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error('useI18n must be used within an I18nProvider');
  }
  return context;
}
